package day15Sep23;

public class Operator {
	public static void main(String[] args) {
		/*
		 * relational operator: < > <= >= == !=
		 * 
		 */
		int a = 10, b = 10;
		System.out.println(a > b); // 10 > 10 false
		System.out.println(a < b); // 10 < 10 false
		System.out.println(a <= b); // 10 <= 10 true
		System.out.println(a == b); // true
		System.out.println(a != b); // 10 != 10 false
		System.out.println(a >= b); // 10 >= 10 true

	}
}
